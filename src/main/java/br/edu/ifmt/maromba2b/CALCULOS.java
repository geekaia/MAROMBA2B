    private void btnCalcIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcIMCActionPerformed
        // TODO add your handling code here:
        String peso  = txtPeso.getText();  
        String altura  = txtAltura.getText();  
        
        try { 
            double pesod = Double.parseDouble(peso);
            double alturad = Double.parseDouble(altura);
            double imcRes = pesod / (alturad*alturad);
            
            String strSit=""; 
            if (imcRes < 18.5) { 
                strSit = "Abaixo do peso"; 
            } else if(imcRes <= 24.9) { 
                strSit = "Peso normal"; 
            } else if(imcRes <= 29.9) { 
                strSit = "Pré-obesidade"; 
            } else if(imcRes <= 34.9) { 
                strSit = "Obesidade Grau 1"; 
            } else if(imcRes <= 39.9) { 
                strSit = "Obesidade Grau 2"; 
            } else {
                strSit = "Obesidade Grau 3"; 
            }
              lbResultado.setText("Situação: "+strSit);  
        }catch(Exception e) { 
            e.printStackTrace();
            lbResultado.setText("Situação: ERRO NA CONVERSÃO");
        }
    }//GEN-LAST:event_btnCalcIMCActionPerformed

