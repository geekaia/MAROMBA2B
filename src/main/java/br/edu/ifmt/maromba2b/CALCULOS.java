    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here: 
       String sexopessoa="Nulo"; 
       
       if (btnMasculino.isSelected()) { 
           sexopessoa="M"; 
       } else if(btnFeminino.isSelected()) {
           sexopessoa="F";            
       }
       System.out.println("Sexo: "+sexopessoa);
       String idade = txtIdade.getValue().toString(); 
       Integer idadei = Integer.parseInt(idade); 
       String peso = txtPesoCalorias.getText();
       Double pesod = Double.parseDouble(peso); 
        double gastoEnergbasal=0.0;
       if (idadei >= 0 && idadei <= 3) {
           if (sexopessoa.equals("F") ){
               gastoEnergbasal = (58.317 * pesod) - 31.1; 
           } else if (sexopessoa.equals("M") ){
               gastoEnergbasal = (59.512 * pesod) - 30.4;  
           }
       } else if ( idadei <= 10) {
           if (sexopessoa.equals("F") ){
               gastoEnergbasal = (20.315 * pesod) - 485.9; 
           } else if (sexopessoa.equals("M") ){
               gastoEnergbasal = (22.706 * pesod) - 504.3;  
           }
       }
       lbResultadoCalorias.setText("Gasto Basal: "+gastoEnergbasal);
 


    }//GEN-LAST:event_btnCalcularActionPerformed

